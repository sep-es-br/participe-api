public class AcSectionInfoDto {
    
    private String guid;
    private String nome;
    private String sigla;
    private String guidOrganizacao;

    public String getGuid() {
        return guid;
    }

    public void setGuid(String guid) {
        this.guid = guid;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getSigla() {
        return sigla;
    }

    public void setSigla(String sigla) {
        this.sigla = sigla;
    }

    public String getGuidOrganizacao() {
        return guidOrganizacao;
    }

    public void setGuidOrganizacao(String guidOrganizacao) {
        this.guidOrganizacao = guidOrganizacao;
    }
    
    
    
}
